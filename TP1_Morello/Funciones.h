/** \brief Función que sirve para sumar dos enteros.
 * \param Primer número entero a sumar.
 * \param Segundo número entero a sumar.
 * \return Devuelve el resultado de la suma.
 */
float suma(float, float);

/** \brief Función que sirve para restar dos enteros.
 * \param Primer número entero a restar.
 * \param Segundo número entero a restar.
 * \return Devuelve el resultado de la resta.
 */
float resta(float, float);

/** \brief Función que sirve para multiplicar dos enteros.
 * \param Primer número entero a multiplicar.
 * \param Segundo número entero a multiplicar.
 * \return Devuelve el producto de la multiplicación.
 */
float multiplicacion(float, float);

/** \brief Función que sirve para dividir dos enteros.
 * \param Primer número entero a dividir.
 * \param Segundo número entero a dividir.
 * \return Devuelve el resultado de la división.
 */
float division(float, float);

/** \brief Función que sirve para sacar el factorial de un número.
 * \param Número ingresado.
 * \return Devuelve el resultado del factorial.
 */
long int factorial(long int);
